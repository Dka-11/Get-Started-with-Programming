/*
Tipe data dalam Js

Undefined
- Tipe data ini terbentuk ketika sebuah variable tidak memiliki nilai.

Contoh :
let score;
console.log(typeof(score)); //Undefined

Fungsi typeof digunakan untuk mengembalikan tipe data tersebut dalam bentuk teks.
*/

/*
Numbers
- Nilai dari tipe number adalah angka. var pada angka dituliskan seperti angka pada sebelumnya.

let score = 100; 

Aritmatika pada number
+ - / * %

Operator aritmatika
Increment (++)
Decrement (--)

Operator diatas dapat dituliskan sebelum atau sesudah variabel. Berikut ketentuannya :
x++ yang berarti  menghasilkan nilai var sebelum ditambahkan nilainya.
++x yang berarti menghasilkan nilai var setelah ditambahkan nilainya.

Contoh :
let postfix = 5;
console.log("Hasil dari postfix++ adalah ", postfix++); // Menghasilkan nilai 5 karena ditampilkan terlebih dahulu baru ditambah
console.log("Sehingga, nilai dari postfix saat ini adalah ", postfix); // Bernilai 6 karena telah ditambah

let prefix = 5;
console.log("Hasil dari ++prefix adalah ", ++prefix); // Mennghasilkan nilai 6 karena menambah nilai terlebih dahulu sebelum ditampilkan
console.log("Sehingga, nilai dari prefix saat ini adalah ", prefix); // tetap bernilai 6 karena sudah ditambah
*/

/*
Strings
Untuk menetapkan nilai string dapat menggunakan petik satu (') atau petik dua (")
Dapat menggunakan tanda petik secara bergantian.

Jika kedua jenis petik tersebut digunakan maka dapat menggunakan backslash (\) untuk mengurangi ambiguitas pada petik
Disebut jg sebagai EscapeString.

const answer = '"I think it\'s great!" i answered'; //output : I think it's great! i answered
console.log(answer);
*/

