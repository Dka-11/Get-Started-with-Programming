/*
Function in JavaScript

Fungsi pada Js dibuat dengan keyword function diikuti dengan nama fungsi tsb.

function greeting(){
    console.log("Selamat Sore!");
}

greeting //pemanggilan fungsi

This is Static Function cant use parameter.
*/

/*
Dynamic Function (using parameter)

Parameter digunakan untuk menampung suatu nilai dari variabel.
Dapat menambah parameter based on kebutuhan.

function greeting(greet) {
    console.log("Selamat " + greet + "!");
}

greeting('malam'); // Selamat malam!
greeting('pagi'); // Selamat pagi!

*/

/*
Tips membuat fungsi di Js
- Hindari pembuatan parameter yg banyak, cukup 1 - 2 parameter saja
- Melakukan satu hal, jika fungsi membuat banyak hal akan sulit untuk disusun dan diuji.
- Nama fungsi mempresentasikan tujuannya
- Buatlah fungsi untuk menghindari duplikasi kode.
*/